int
pong_main(void) {

    /* Initialize the ball position data. */
    init_ball();

    Uint32 next_game_tick = SDL_GetTicks();
    int state = 0;
    int r = 0;
    int quit = 0;
    int sleep = 0;

    /* Animate */
    while (quit == 0) {
        sleep = 50;
        
        buttonUpdate();
        if (button.NoUp)
        {
            move_paddle(0);
        }
        else if(button.YesUp)
        {
            move_paddle(1);
        }
        
        // draw the background
        draw_background();
        draw_net();

        // display main menu
        if (state == 0 ) {
        
            if (button.NoUp || button.YesUp)
            {
                state = 1;
            }
        
            // draw menu 
            draw_menu();
        
        // display gameover
        } else if (state == 2) {
        
            if (button.NoDown && button.YesDown)
            {
                state = 0;
                // delay for a little bit so the space bar press dosnt get triggered twice
                // while the main menu is showing
                SDL_Delay(500);
            }

            // display gameover
            draw_game_over(r);

            if (button.NoUp || button.YesUp)
            {
                state = 1;
            }
                
        // display the game
        } else if (state == 1){
            
            // check score
            r = check_score();
            
            if (r == 1) {
                
                state = 2;  

            } else if (r == 2){
            
                state = 2;  
            }

            // paddle ai movement
            move_paddle_ai();

            /* Move the balls for the next frame. */
            move_ball();
            
            // draw net
            draw_net();

            // draw paddles
            draw_paddle();
            
            /* Put the ball on the screen. */
            draw_ball();
    
            // draw the score
            draw_player_0_score();
    
            // draw the score
            draw_player_1_score();
        }

        /* Ask SDL to update the entire screen. */
        SDL_Flip(&screen);

        next_game_tick += 1000 / 10;
        sleep = next_game_tick - SDL_GetTicks();
    
        if( sleep >= 0 )
        {
            SDL_Delay(sleep);
        }
    }
    
    return 0;
}
