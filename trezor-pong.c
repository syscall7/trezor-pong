static const BITMAP bmp_title = {128, 64, bmp_title_data};
static const BITMAP bmp_game_over = {128, 64, bmp_game_over_data};

SDL_Surface screen = {
    .w = OLED_HEIGHT,
    .h = OLED_WIDTH
};

// initialize starting position and sizes of game elemements
static void
init_ball(void) {
    
    ball.x = screen.w / 2;
    ball.y = screen.h / 2;
    ball.w = 1;
    ball.h = 1;
    ball.dy = 1;
    ball.dx = 1;
    
    paddle[0].x = 2;
    paddle[0].y = screen.h / 2 - 5;
    paddle[0].w = 1;
    paddle[0].h = 15;

    paddle[1].x = screen.w - 2 - 1;
    paddle[1].y = screen.h / 2 - 5;
    paddle[1].w = 1;
    paddle[1].h = 15;
}

static int
check_score(void) {
    
    int i;

    // loop through player scores
    for(i = 0; i < 2; i++) {
    
        // check if score is @ the score win limit
        if (score[i] == WINNING_SCORE ) {
        
            // reset scores
            score[0] = 0;
            score[1] = 0;
            
            // return 1 if player 1 score @ limit
            if (i == 0) {

                return 1;   

            // return 2 if player 2 score is @ limit
            } else {
                
                return 2;
            }
        }
    }
    
    // return 0 if no one has reached a score of WINNING_SCORE yet
    return 0;
}

// if return value is 1 collision occured. if return is 0, no collision.
static int
check_collision(ball_t a, paddle_t b) {

    int left_a, left_b;
    int right_a, right_b;
    int top_a, top_b;
    int bottom_a, bottom_b;

    left_a = a.x;
    right_a = a.x + a.w;
    top_a = a.y;
    bottom_a = a.y + a.h;

    left_b = b.x;
    right_b = b.x + b.w;
    top_b = b.y;
    bottom_b = b.y + b.h;
    

    if (left_a > right_b) {
        return 0;
    }

    if (right_a < left_b) {
        return 0;
    }

    if (top_a > bottom_b) {
        return 0;
    }

    if (bottom_a < top_b) {
        return 0;
    }

    return 1;
}

/* This routine moves each ball by its motion vector. */
static void
move_ball(void) {
    
    /* Move the ball by its motion vector. */
    ball.x += ball.dx;
    ball.y += ball.dy;
    
    /* Turn the ball around if it hits the edge of the screen. */
    if (ball.x < 0) {
        
        score[1] += 1;
        init_ball();
    }

    if (ball.x > screen.w - 1) { 
        
        score[0] += 1;
        init_ball();
    }

    if (ball.y < 0 || ball.y > screen.h - 1) {
        
        ball.dy = -ball.dy;
    }

    // check for collision with the paddle
    int i;

    for (i = 0; i < 2; i++) {
        
        int c = check_collision(ball, paddle[i]); 

        // collision detected    
        if (c == 1) {
            
            // ball moving left
            if (ball.dx < 0) {
                    
                ball.dx -= 1;

            // ball moving right
            } else {
                    
                ball.dx += 1;
            }
            
            // change ball direction
            ball.dx = -ball.dx;
            
            // change ball angle based on where on the paddle it hit
            int hit_pos = (paddle[i].y + paddle[i].h) - ball.y;

            if (hit_pos >= 0 && hit_pos < 1) {
                ball.dy = 4;
            }

            if (hit_pos >= 1 && hit_pos < 2) {
                ball.dy = 3;
            }
            
            if (hit_pos >= 2 && hit_pos < 4) {
                ball.dy = 2;
            }

            if (hit_pos >= 4 && hit_pos < 6) {
                ball.dy = 1;
            }

            if (hit_pos >= 6 && hit_pos < 9) {
                ball.dy = 0;
            }

            if (hit_pos >= 9 && hit_pos < 11) {
                ball.dy = -1;
            }

            if (hit_pos >= 11 && hit_pos < 13) {
                ball.dy = -2;
            }

            if (hit_pos >= 13 && hit_pos < 14) {
                ball.dy = -3;
            }

            if (hit_pos >= 14 && hit_pos <= 15) {
                ball.dy = -4;
            }

            // ball moving right
            if (ball.dx > 0) {

                // teleport ball to avoid mutli collision glitch
                if (ball.x < 1) {
                
                    ball.x = 1;
                }
                
            // ball moving left
            } else {
                
                // teleport ball to avoid mutli collision glitch
                if (ball.x > 127) {
                
                    ball.x = 127;
                }
            }
        }
    }
}

static void
move_paddle_ai(void) {

    int center = paddle[0].y;
    int screen_center = screen.h / 2;
    int ball_speed = ball.dy;

    if (ball_speed < 0) {
    
        ball_speed = -ball_speed;
    }

    // ball moving right
    if (ball.dx > 0) {
        
        // return to center position
        if (center < screen_center) {
            
            paddle[0].y += ball_speed;
        
        } else {
        
            paddle[0].y -= ball_speed;  
        }

    // ball moving left
    } else {
    
        // ball moving down
        if (ball.dy > 0) { 
            
            if (ball.y > center) { 
                
                paddle[0].y += ball_speed;

            } else { 
            
                paddle[0].y -= ball_speed;
            }
        }
        
        // ball moving up
        if (ball.dy < 0) {
        
            if (ball.y < center) { 
                
                paddle[0].y -= ball_speed;
            
            } else {
            
                paddle[0].y += ball_speed;
            }
        }

        // ball moving stright across
        if (ball.dy == 0) {
            
            if (ball.y < center) { 
            
                paddle[0].y -= 5;

            } else {
            
                paddle[0].y += 5;
            }
        }           
    }
}

static void
move_paddle(int d) {

    // if the down arrow is pressed move paddle down
    if (d == 0) {
        
        if(paddle[1].y >= screen.h - paddle[1].h) {
        
            paddle[1].y = screen.h - paddle[1].h;
        
        } else { 
        
            paddle[1].y += 5;
        }
    }
    
    // if the up arrow is pressed move paddle up
    if (d == 1) {

        if(paddle[1].y <= 0) {
        
            paddle[1].y = 0;

        } else {
        
            paddle[1].y -= 5;
        }
    }
}

static void
draw_game_over(int p) { 

    char *winner;
    if (p == 2)
    {
        winner = "Player 1 wins!";
    }
    else
    {
        winner = "Computer wins!";
    }

    oledDrawBitmap(0, 0, &bmp_game_over);
    oledDrawStringCenter(8, winner, FONT_STANDARD);
    oledDrawStringCenter(OLED_HEIGHT - 8, "Press any button to begin!", FONT_STANDARD);
    return;
}

